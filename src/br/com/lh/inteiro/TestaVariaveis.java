package br.com.lh.inteiro;

public class TestaVariaveis {

	public static void main(String[] args) {

		// variável inteira int -2.147.483.648 á 2.147.483.647
		// posso declarar com número assim
		int idade = 20;
		// ou idade = 21;

		System.out.println(idade);

		idade = 30 + 10;
		System.out.println(idade);
		
		//como na matemática o calculo começa dentro de parênteses
		idade = (7 * 5) + 2;

		System.out.println(idade);

	}

}
